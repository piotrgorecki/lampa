function shuffle<T>(a: Array<T>): Array<T> {
    for (let i = a.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [a[i], a[j]] = [a[j], a[i]];
    }
    return a;
}

let counter = -1;
let list = shuffle<ImageSourcePropType>(Lamps);

const getNextImage = (): ImageSourcePropType => {
    counter++;
    if (counter >= list.length) {
        list = shuffle<ImageSourcePropType>(Lamps);
        counter = 0;
    }

    return list[counter];
};

export default getNextImage;
